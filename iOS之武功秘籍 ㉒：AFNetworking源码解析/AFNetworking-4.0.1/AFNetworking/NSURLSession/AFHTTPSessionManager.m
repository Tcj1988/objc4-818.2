/*
1.调用父类的方法
2.给url添加“/”
3.给requestSerializer、responseSerializer设置默认值
*/
- (instancetype)initWithBaseURL:(NSURL *)url
           sessionConfiguration:(NSURLSessionConfiguration *)configuration
{
    //调用父类初始化方法 -- 为什么这样写?不用想里面肯定有东西...
    self = [super initWithSessionConfiguration:configuration];
    if (!self) {
        return nil;
    }

    // Ensure terminal slash for baseURL path, so that NSURL +URLWithString:relativeToURL: works as expected
    /*
     为了确保NSURL +URLWithString:relativeToURL: works可以正确执行，在baseurlpath的最后添加‘/’
     */
    // url有值且没有‘/’,那么在url的末尾添加‘/’
    // 如果不带`/`会有一个默认的重定向 -- www.baidu.com 2次 浪费带宽
    // 表示为目录:后面会有一系列信息的拼接 路由的拼接 如username=tcj --- 我们在request 封装在讲
    // SEO: 当前在我们相应的排名
    if ([[url path] length] > 0 && ![[url absoluteString] hasSuffix:@"/"]) {
        url = [url URLByAppendingPathComponent:@""];
    }

    self.baseURL = url;
    //给requestSerializer、responseSerializer设置默认值
    // http : 请求行+请求头+请求体
    self.requestSerializer = [AFHTTPRequestSerializer serializer];
    self.responseSerializer = [AFJSONResponseSerializer serializer];

    return self;
}

